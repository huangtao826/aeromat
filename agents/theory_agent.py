    def explain(self, topic: str, intent: str, difficulty: str, context: str = "") -> str:
        """解释理论知识点"""
        topic_kb = self.THEORY_KB.get(topic, {})

        if not topic_kb:
            return self._generate_general_explanation(topic, context)

        if difficulty == "advanced" and "advanced" in topic_kb:
            explanation = topic_kb["advanced"]
        elif "intermediate" in topic_kb:
            explanation = topic_kb["intermediate"]
        else:
            explanation = topic_kb.get("basic", "")

        # 根据意图补充内容
        if intent == "optimization":
            explanation += self._get_optimization_guidance(topic)

        return explanation

